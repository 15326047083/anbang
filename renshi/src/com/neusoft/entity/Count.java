package com.neusoft.entity;

public class Count {
	private int id;
	private String deptName;
	private int firstNum;
	private int endNum;
	private int newNum;
	private int quitNum;
	private int thisIn;
	private int thisOut;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public int getFirstNum() {
		return firstNum;
	}
	public void setFirstNum(int firstNum) {
		this.firstNum = firstNum;
	}
	public int getEndNum() {
		return endNum;
	}
	public void setEndNum(int endNum) {
		this.endNum = endNum;
	}
	public int getNewNum() {
		return newNum;
	}
	public void setNewNum(int newNum) {
		this.newNum = newNum;
	}
	public int getQuitNum() {
		return quitNum;
	}
	public void setQuitNum(int quitNum) {
		this.quitNum = quitNum;
	}
	public int getThisIn() {
		return thisIn;
	}
	public void setThisIn(int thisIn) {
		this.thisIn = thisIn;
	}
	public int getThisOut() {
		return thisOut;
	}
	public void setThisOut(int thisOut) {
		this.thisOut = thisOut;
	}
}
